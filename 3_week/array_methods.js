// method shift
let frutas = ["morango", "banana", "limao"];
let first_fruta = frutas.shift();
console.log(frutas);
console.log(first_fruta);

// method pop, remove ultimo elemento
let cores = ["azul", "amarelo", "vermelho"];
let remove_cores = cores.pop();
console.log(cores);
console.log(remove_cores);

//method unshift, adiciona elemento no inicio
let marcas = ["apple", "amazon", "kimetch"];
let unshift_marcas = marcas.unshift("google");
console.log(marcas);

//method push, adiciona elemento no final
let livros = ["algoritmo", "estrurua de dados", "entendendo"];
livros.push("Como ser");
console.log(livros);
